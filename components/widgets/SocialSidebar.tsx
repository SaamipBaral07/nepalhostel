export function SocialSidebar() {
  const [links, setLinks] = useState<SocialLink[]>([]);

  useEffect(() => {
    widgetsApi
      .getSocialLinks()
      .then((res) => setLinks(res.data))
      .catch(() => {});
  }, []);

  if (links.length === 0) return null;

  return (
    <div className="fixed right-0 top-1/2 -translate-y-1/2 z-40 hidden sm:flex flex-col">
      <div className="flex flex-col gap-0.5 rounded-l-2xl bg-white/90 backdrop-blur-md shadow-lg shadow-black/5 border border-r-0 border-zinc-200/60 p-1.5">
        {links.map((link) => {
          const platform = PLATFORM_ICONS[link.platform] || PLATFORM_ICONS.other;
          return (
            <a
              key={link.id}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              title={link.label || link.platform}
              className={`group relative flex h-10 w-10 items-center justify-center rounded-xl text-zinc-500 transition-all duration-200 ${platform.color}`}
            >
              {platform.svg}
              {/* Tooltip */}
              <span className="pointer-events-none absolute right-full mr-3 whitespace-nowrap rounded-lg bg-zinc-900 px-3 py-1.5 text-xs font-medium text-white opacity-0 shadow-lg transition-all duration-200 group-hover:opacity-100 group-hover:mr-2">
                {link.label || link.platform}
                <span className="absolute top-1/2 -right-1 -translate-y-1/2 border-4 border-transparent border-l-zinc-900" />
              </span>
            </a>
          );
        })}
      </div>
    </div>
  );
}
